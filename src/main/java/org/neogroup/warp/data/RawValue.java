package org.neogroup.warp.data;

/**
 *
 */
public class RawValue {

    private final String value;

    /**
     *
     * @param value
     */
    public RawValue(String value) {
        this.value = value;
    }

    /**
     *
     * @return
     */
    public String getValue() {
        return value;
    }
}
